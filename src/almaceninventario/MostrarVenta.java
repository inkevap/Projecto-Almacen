    public MostrarVenta(Venta venta) {
        initComponents();
        DefaultTableModel tabla = (DefaultTableModel) jTableVenta.getModel();
        jLabel7.setText(jLabel7.getText()+venta.FechaCreacion);
        jLabel1.setText(jLabel1.getText()+venta.Nombre);
        jLabel8.setText(jLabel8.getText()+venta.NIT);
        jLabel9.setText(jLabel9.getText()+venta.NoVenta);
        jLabel6.setText(jLabel6.getText()+venta.total);
        for (ProductoVendido producto : venta.ProductosVendidos) {

            Object[] row = {
                producto.Nombre,
                producto.Codigo,
                producto.Descripcion,
                producto.EstadoProducto,
                producto.Cantidad,
                producto.PrecioUnidad,
                producto.Subtotal,
            };
            tabla.addRow(row);
        }
    }

